<?php
// While loop
$i = 1;
while ($i <= 5) {
    echo "The number is: $i <br>";
    $i++;
}

// For loop
for ($j = 0; $j <= 10; $j++) {
    echo "The number is: $j <br>";
}

// Foreach loop
$colors = array("red", "green", "blue");
foreach ($colors as $color) {
    echo "Color: $color <br>";
}
?>
